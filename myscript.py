#!/usr/bin/env python3

def main():
    # ask for user name
    name = input("what is your name?:")

    # ask for day of the week
    day = input("what is the day of the week?:")

    # prints name and day of the week from user
    print(f"Hello, {name}! Happy {day}!")

if __name__ == "__main__": 
    main()    
